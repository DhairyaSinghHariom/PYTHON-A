# integer

INT = 'int'
a = 5
b = 10
c = a + b
print(f"Integer addition: {a} + {b} = {c}")     

# float

FLOAT = 'float'
x = 5.5
y = 2.3
z = x * y
print(f"Float multiplication: {x} * {y} = {z}")# Input      


# complex

COMPLEX = 'complex'
p = 2 + 3j      
q = 1 + 4j
r = p + q
print(f"Complex addition: {p} + {q} = {r}") 

# string

STRING = 'string'
str1 = "Hello, "
str2 = "World!"
str3 = str1 + str2
print(f"String concatenation: '{str1}' + '{str2}' = '{str3}'")

# boolean

BOOLEAN = 'boolean'
is_true = True      
is_false = False
result = is_true and is_false
print(f"Boolean AND: {is_true} and {is_false} = {result}")      


#tuple

TUPLE = 'tuple'
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
tuple3 = tuple1 + tuple2
print(f"Tuple concatenation: {tuple1} + {tuple2} = {tuple3}")

# list

LIST = 'list'
list1 = [1, 2, 3]
list2 = [4, 5, 6]
list3 = list1 + list2
print(f"List concatenation: {list1} + {list2} = {list3}")

# dictionary
DICTIONARY = 'dictionary'
dict1 = {'a': 1, 'b': 2}
dict2 = {'c': 3, 'd': 4}
dict3 = {**dict1, **dict2}
print(f"Dictionary merge: {dict1} + {dict2} = {dict3}")

#set
SET = 'set'
set1 = {1, 2, 3}
set2 = {3, 4, 5}
set3 = set1.union(set2)
print(f"Set union: {set1} ∪ {set2} = {set3}")

